# Imprimir los numeros enteros del 0 al 5 utilizando un ciclo While
numero = 0

while numero <= 5:
    print(numero)
    # numero = numero + 1
    numero += 1
else:
    print("Finalizo...")

# Imprimir 5 al 1 de manera descendente
numero = 5
numeroFinal = 1
while numeroFinal <= numero:
    print(numero)
    # numero = numero - 1
    numero -= 1
else:
    print("Finalizo de manera satisfactoria")